#!/usr/bin/env python3

import argparse
import csv
from pathlib import Path

import matplotlib.pyplot as plt


def to_float(value, default=0.0):
    try:
        return float(value)
    except (TypeError, ValueError):
        return default


def load_rows(path: Path):
    with path.open("r", encoding="utf-8") as f:
        return list(csv.DictReader(f))


def pick_row(rows, attack_type, is_defense):
    for row in rows:
        if row.get("attack_type") != attack_type:
            continue
        scenario = row.get("scenario", "")
        if is_defense and "combined_defense" in scenario:
            return row
        if not is_defense and "baseline" in scenario:
            return row
    return None


def metric_values(rows, attack_type, metric):
    baseline = pick_row(rows, attack_type, is_defense=False)
    defense = pick_row(rows, attack_type, is_defense=True)
    return (
        to_float(baseline.get(metric) if baseline else None),
        to_float(defense.get(metric) if defense else None),
    )


def error_rate_values(rows, attack_type):
    baseline = pick_row(rows, attack_type, is_defense=False)
    defense = pick_row(rows, attack_type, is_defense=True)

    def rate(row):
        if not row:
            return 0.0
        sent = to_float(row.get("attacker_packets_sent"))
        errs = to_float(row.get("attacker_send_errors"))
        denom = sent + errs
        if denom <= 0:
            return 0.0
        return (errs / denom) * 100.0

    return rate(baseline), rate(defense)


def bars_with_labels(ax, labels, baseline_vals, defense_vals, title, ylabel):
    x = range(len(labels))
    width = 0.35
    baseline_bars = ax.bar([i - width / 2 for i in x], baseline_vals, width=width, label="baseline", color="#457B9D")
    defense_bars = ax.bar([i + width / 2 for i in x], defense_vals, width=width, label="combined_defense", color="#2A9D8F")

    ax.set_xticks(list(x))
    ax.set_xticklabels(labels)
    ax.set_title(title)
    ax.set_ylabel(ylabel)
    ax.grid(axis="y", alpha=0.25)
    ax.legend()

    for bar in list(baseline_bars) + list(defense_bars):
        h = bar.get_height()
        ax.text(bar.get_x() + bar.get_width() / 2, h, f"{h:.2f}", ha="center", va="bottom", fontsize=8)


def main():
    parser = argparse.ArgumentParser(description="Plot combined auth/property defense experiment results")
    parser.add_argument("--summary-csv", required=True, help="Summary CSV generated by runner")
    parser.add_argument("--output-dir", required=True, help="Directory for plot outputs")
    args = parser.parse_args()

    summary_csv = Path(args.summary_csv)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    rows = load_rows(summary_csv)
    if not rows:
        raise RuntimeError(f"No rows found in summary CSV: {summary_csv}")

    attack_labels = ["auth", "property"]
    cpu_baseline = []
    cpu_defense = []
    mem_baseline = []
    mem_defense = []
    thr_baseline = []
    thr_defense = []
    err_rate_baseline = []
    err_rate_defense = []

    for attack in attack_labels:
        b, d = metric_values(rows, attack, "broker_cpu_peak_pct")
        cpu_baseline.append(b)
        cpu_defense.append(d)
        b, d = metric_values(rows, attack, "broker_mem_peak_mib")
        mem_baseline.append(b)
        mem_defense.append(d)
        b, d = metric_values(rows, attack, "attacker_attempt_rate_msgs_per_s")
        thr_baseline.append(b)
        thr_defense.append(d)
        b, d = error_rate_values(rows, attack)
        err_rate_baseline.append(b)
        err_rate_defense.append(d)

    plt.style.use("seaborn-v0_8-whitegrid")
    fig, axes = plt.subplots(2, 2, figsize=(13, 9))
    bars_with_labels(axes[0, 0], attack_labels, cpu_baseline, cpu_defense, "Broker CPU Peak by Attack Type", "CPU peak (%)")
    bars_with_labels(axes[0, 1], attack_labels, mem_baseline, mem_defense, "Broker Memory Peak by Attack Type", "Memory peak (MiB)")
    bars_with_labels(axes[1, 0], attack_labels, thr_baseline, thr_defense, "Attacker Attempt Rate by Attack Type", "Attempted packets/s")
    bars_with_labels(axes[1, 1], attack_labels, err_rate_baseline, err_rate_defense, "Attacker Send Error Rate by Attack Type", "Error rate (%)")
    fig.tight_layout()
    fig.savefig(output_dir / "combined_auth_property_overview.png", dpi=140)
    plt.close(fig)

    with (output_dir / "plots_manifest.csv").open("w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["plot_file"])
        writer.writerow(["combined_auth_property_overview.png"])


if __name__ == "__main__":
    main()
