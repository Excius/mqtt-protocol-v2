#!/usr/bin/env python3

import argparse
import csv
from pathlib import Path

import matplotlib.pyplot as plt


PROFILE_ORDER = {"LOW_POWER": 0, "BALANCED": 1, "HIGH_SECURITY": 2}


def to_float(value, default=0.0):
    try:
        return float(value)
    except (TypeError, ValueError):
        return default


def load_rows(path: Path):
    with path.open("r", encoding="utf-8") as f:
        rows = [row for row in csv.DictReader(f) if row.get("profile")]
    return sorted(rows, key=lambda r: (PROFILE_ORDER.get(r["profile"], 99), r["profile"]))


def label_bars(ax, bars):
    for bar in bars:
        val = bar.get_height()
        ax.text(bar.get_x() + bar.get_width() / 2, val, f"{val:.2f}", ha="center", va="bottom", fontsize=8)


def main():
    parser = argparse.ArgumentParser(description="Plot TLS profiles + session resumption combined results")
    parser.add_argument("--summary-csv", required=True, help="Summary CSV generated by combined runner")
    parser.add_argument("--output-dir", required=True, help="Plot output directory")
    args = parser.parse_args()

    summary_csv = Path(args.summary_csv)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    rows = load_rows(summary_csv)
    if not rows:
        raise RuntimeError(f"No rows found in summary CSV: {summary_csv}")

    profiles = [r["profile"] for r in rows]
    handshake = [to_float(r.get("handshake_avg_ms")) for r in rows]
    broker_cpu = [to_float(r.get("broker_cpu_avg_pct")) for r in rows]
    client_cpu = [to_float(r.get("client_cpu_avg_pct")) for r in rows]
    throughput = [to_float(r.get("throughput_msgs_per_s")) for r in rows]

    plt.style.use("seaborn-v0_8-whitegrid")
    fig, axes = plt.subplots(2, 2, figsize=(12, 8))

    bars = axes[0, 0].bar(profiles, handshake, color="#2A9D8F")
    axes[0, 0].set_title("Handshake Avg (ms)")
    axes[0, 0].set_ylabel("ms")
    axes[0, 0].grid(axis="y", alpha=0.25)
    label_bars(axes[0, 0], bars)

    bars = axes[0, 1].bar(profiles, broker_cpu, color="#3A86FF")
    axes[0, 1].set_title("Broker CPU Avg (%)")
    axes[0, 1].set_ylabel("%")
    axes[0, 1].grid(axis="y", alpha=0.25)
    label_bars(axes[0, 1], bars)

    bars = axes[1, 0].bar(profiles, client_cpu, color="#8338EC")
    axes[1, 0].set_title("Client CPU Avg (%)")
    axes[1, 0].set_ylabel("%")
    axes[1, 0].grid(axis="y", alpha=0.25)
    label_bars(axes[1, 0], bars)

    bars = axes[1, 1].bar(profiles, throughput, color="#1D4E89")
    axes[1, 1].set_title("Throughput (msg/s)")
    axes[1, 1].set_ylabel("msg/s")
    axes[1, 1].grid(axis="y", alpha=0.25)
    label_bars(axes[1, 1], bars)

    fig.tight_layout()
    fig.savefig(output_dir / "tls_resumption_combined_overview.png", dpi=140)
    plt.close(fig)

    with (output_dir / "tls_resumption_combined_manifest.csv").open("w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["plot_file"])
        writer.writerow(["tls_resumption_combined_overview.png"])


if __name__ == "__main__":
    main()
