#!/usr/bin/env python3

import argparse
import csv
from pathlib import Path

import matplotlib.pyplot as plt


def load_csv(path: Path):
    rows = []
    if not path.exists():
        return rows
    with path.open("r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            rows.append(row)
    return rows


def to_float(value, default=0.0):
    try:
        return float(value)
    except (TypeError, ValueError):
        return default


def success_values(rows, key):
    values = []
    for row in rows:
        if row.get("success", "").lower() != "true":
            continue
        raw = row.get(key)
        if raw in (None, ""):
            continue
        values.append(to_float(raw))
    return values


def cdf_points(values):
    if not values:
        return [], []
    sorted_vals = sorted(values)
    n = len(sorted_vals)
    xs = sorted_vals
    ys = [(i + 1) / n for i in range(n)]
    return xs, ys


def save_reconnect_cdf(reconnect_by_scenario, out_path: Path):
    plt.figure(figsize=(10, 5))
    for scenario, values in reconnect_by_scenario.items():
        xs, ys = cdf_points(values)
        if xs:
            plt.plot(xs, ys, linewidth=2, label=scenario)

    plt.xlabel("Reconnect latency (ms)")
    plt.ylabel("CDF")
    plt.title("Reconnect Latency CDF (Old vs New TLS)")
    plt.grid(alpha=0.3)
    plt.legend()
    plt.tight_layout()
    plt.savefig(out_path, dpi=140)
    plt.close()


def save_connect_reconnect_bar(summary_rows, out_path: Path):
    scenarios = [r.get("scenario", "") for r in summary_rows]
    connect_avg = [to_float(r.get("connect_avg_ms")) for r in summary_rows]
    reconnect_avg = [to_float(r.get("reconnect_avg_ms")) for r in summary_rows]

    x = range(len(scenarios))
    width = 0.35

    fig, ax = plt.subplots(figsize=(10, 5))
    ax.bar([i - width / 2 for i in x], connect_avg, width=width, label="connect_avg_ms", color="#30638E")
    ax.bar([i + width / 2 for i in x], reconnect_avg, width=width, label="reconnect_avg_ms", color="#00798C")

    ax.set_xticks(list(x))
    ax.set_xticklabels(scenarios, rotation=20, ha="right")
    ax.set_ylabel("Latency (ms)")
    ax.set_title("Average Connect vs Reconnect Latency")
    ax.grid(axis="y", alpha=0.25)
    ax.legend()
    fig.tight_layout()
    fig.savefig(out_path, dpi=140)
    plt.close(fig)


def save_speedup_bar(summary_rows, out_path: Path):
    scenarios = [r.get("scenario", "") for r in summary_rows]
    speedups = [to_float(r.get("reconnect_speedup_x")) for r in summary_rows]

    fig, ax = plt.subplots(figsize=(8, 4.8))
    bars = ax.bar(scenarios, speedups, color=["#D1495B", "#EDAe49"])
    ax.set_ylabel("Speedup (first_avg / reconnect_avg)")
    ax.set_title("Reconnect Speedup from Session Resumption")
    ax.grid(axis="y", alpha=0.25)
    ax.tick_params(axis="x", rotation=20)

    for bar, value in zip(bars, speedups):
        ax.text(bar.get_x() + bar.get_width() / 2, value, f"{value:.2f}", ha="center", va="bottom")

    fig.tight_layout()
    fig.savefig(out_path, dpi=140)
    plt.close(fig)


def main():
    parser = argparse.ArgumentParser(description="Plot TLS session resumption comparison outputs")
    parser.add_argument("--summary-csv", required=True, help="Summary CSV generated by the comparison script")
    parser.add_argument("--raw-dir", required=True, help="Raw directory containing per-scenario reconnect CSV files")
    parser.add_argument("--output-dir", required=True, help="Directory to write plot files")
    args = parser.parse_args()

    summary_csv = Path(args.summary_csv)
    raw_dir = Path(args.raw_dir)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    summary_rows = load_csv(summary_csv)
    if len(summary_rows) < 2:
        raise RuntimeError(f"Expected at least two scenarios in summary CSV: {summary_csv}")

    reconnect_by_scenario = {}
    for row in summary_rows:
        scenario = row.get("scenario", "")
        reconnect_csv = row.get("reconnect_csv", "")
        reconnect_path = Path(reconnect_csv)
        if not reconnect_path.is_absolute():
            reconnect_path = raw_dir / scenario / "reconnect_latency.csv"
        reconnect_rows = load_csv(reconnect_path)
        reconnect_vals = success_values(reconnect_rows, "reconnect_ms")
        if not reconnect_vals:
            raise RuntimeError(f"No successful reconnect samples for scenario {scenario}: {reconnect_path}")
        reconnect_by_scenario[scenario] = reconnect_vals

    plt.style.use("seaborn-v0_8-whitegrid")
    save_reconnect_cdf(reconnect_by_scenario, output_dir / "reconnect_cdf.png")
    save_connect_reconnect_bar(summary_rows, output_dir / "avg_connect_reconnect.png")
    save_speedup_bar(summary_rows, output_dir / "reconnect_speedup.png")

    with (output_dir / "plots_manifest.csv").open("w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["plot_file"])
        writer.writerow(["reconnect_cdf.png"])
        writer.writerow(["avg_connect_reconnect.png"])
        writer.writerow(["reconnect_speedup.png"])


if __name__ == "__main__":
    main()
